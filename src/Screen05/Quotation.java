// List5-13
public class Quotation {
    public static void main(String[] args) {
        // 拡張表記\"と\'の利用例
        System.out.println("文字列リテラルと文字定数について");

        System.out.println("二重引用符で囲まれた\"ABC\"は文字列リテラルです");

        System.out.print("一重引用符で囲まれた");
        System.out.print('\'');
        System.out.println("A'は文字リテラルです。");
    }
}
