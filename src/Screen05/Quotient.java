package Screen05;

// List5-5
public class Quotient {
    public static void main(String[] args) {
        // 2つの数値の商を求める
        System.out.println("15   / 2   = " + 15   / 2);
        System.out.println("15.0 / 2.0 = " + 15.0 / 2.0);
        System.out.println("15.0 / 2   = " + 15.0 / 2);
        System.out.println("15   / 2.0 = " + 15   / 2.0);
    }
}
